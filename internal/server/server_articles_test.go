// TestDeleteArticle covers the delete criterion: with content.delete → 204
// + row gone; without → 403.
func TestDeleteArticle(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.create", "content.delete")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	authorToken := jwtFor(t, db, "au@example.com", "pw", "author") // no delete

	id := createArticle(t, srv, editorToken, "Bye", "Body")

	// Without perm → 403, row still present.
	if status, _ := doJSON(t, srv, http.MethodDelete, "/articles/"+id, nil, authHeader(authorToken)); status != http.StatusForbidden {
		t.Fatalf("without-perm status=%d, want 403", status)
	}
	if !exists(t, db, id) {
		t.Fatal("row disappeared after 403")
	}

	// With perm → 204 + row gone.
	status, _ := doJSON(t, srv, http.MethodDelete, "/articles/"+id, nil, authHeader(editorToken))
	if status != http.StatusNoContent && status != http.StatusOK {
		t.Fatalf("with-perm status=%d, want 204/200", status)
	}
	if exists(t, db, id) {
		t.Fatal("row still present after delete")
	}
}

// exists reports whether an article row with the given id is present.
func exists(t *testing.T, db *sql.DB, id string) bool {
	t.Helper()
	var x int
	err := db.QueryRow(`SELECT 1 FROM articles WHERE id = ?`, id).Scan(&x)
	if err == sql.ErrNoRows {
		return false
	}
	if err != nil {
		t.Fatalf("exists check: %v", err)
	}
	return true
}

// --- Red-team: 404 paths (missing + malformed id) -------------------------

// TestNotFound confirms every gated route returns 404 — never 500/panic — for
// a valid-format id that does not exist.
func TestNotFound(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.update", "content.publish", "content.delete")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	h := authHeader(editorToken)

	cases := []struct {
		method, path string
	}{
		{http.MethodGet, "/articles/" + nonexistentID},
		{http.MethodPut, "/articles/" + nonexistentID},
		{http.MethodPost, "/articles/" + nonexistentID + "/publish"},
		{http.MethodDelete, "/articles/" + nonexistentID},
	}
	for _, c := range cases {
		status, body := doJSON(t, srv, c.method, c.path,
			map[string]string{"title": "x", "body": "y"}, h)
		if status != http.StatusNotFound {
			t.Fatalf("%s %s status=%d body=%v, want 404", c.method, c.path, status, body)
		}
	}
}

// TestMalformedIDIsNotFound is the explicit red-team check: a non-UUID id in
// the URL must NOT yield 500 or a panic — it surfaces as 404 (no row matches).
func TestMalformedIDIsNotFound(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.update", "content.publish", "content.delete")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	h := authHeader(editorToken)

	cases := []struct {
		method, path string
	}{
		{http.MethodGet, "/articles/not-a-uuid"},
		{http.MethodPut, "/articles/x'OR'1'='1"},
		{http.MethodPost, "/articles/!!!/publish"},
		{http.MethodDelete, "/articles/abc"},
	}
	for _, c := range cases {
		status, body := doJSON(t, srv, c.method, c.path,
			map[string]string{"title": "x", "body": "y"}, h)
		if status != http.StatusNotFound {
			t.Fatalf("%s %s status=%d body=%v, want 404 (not 500)", c.method, c.path, status, body)
		}
	}

	// Sanity: the articles table still exists (an injection-style id did
	// nothing — the query is parameterized).
	var n int
	if err := db.QueryRow(`SELECT COUNT(*) FROM articles`).Scan(&n); err != nil {
		t.Fatalf("articles table missing after malformed-id attempts: %v", err)
	}
}
