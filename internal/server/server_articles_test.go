// server_articles_test.go implements CONTRACT-03 T4: end-to-end authorization
// tests over the articles CRUD surface. It reuses the helpers in
// server_auth_test.go (openAuthMux, doJSON, roleID, testSecret, timeNowUTC) —
// same package — and adds a few small grant/identity helpers below.
//
// Role/permission setup note: SeedCatalogs (CONTRACT-02) seeds only the role
// and permission *names*; role_permissions (which role has which permission)
// is not seeded. Each test grants the specific permissions it needs to the
// roles it uses, so the grant state is explicit and per-test.
//
//   - "editor"   → granted all four content.* permissions; the "with perm"
//                 identity and the fixture creator.
//   - "author"   → granted NO permissions; the authenticated-but-without-perm
//                 identity (403 on gated routes, 200 on reads).
//   - API keys   → minted against "editor" (has perms) to exercise the
//                 service-identity path, including the design-decision reject
//                 on POST.

// permissionID looks up a permission id by name.
func permissionID(t *testing.T, db *sql.DB, name string) string {
	t.Helper()
	var id string
	if err := db.QueryRow(`SELECT id FROM permissions WHERE name = ?`, name).Scan(&id); err != nil {
		t.Fatalf("lookup permission %q: %v", name, err)
	}
	return id
}

// grant inserts role_permissions rows for the given role/permissions
// idempotently. It is the test-only equivalent of an admin assigning a
// permission to a role.
func grant(t *testing.T, db *sql.DB, role string, perms ...string) {
	t.Helper()
	rid := roleID(t, db, role)
	for _, p := range perms {
		pid := permissionID(t, db, p)
		if _, err := db.Exec(
			`INSERT INTO role_permissions (role_id, permission_id) VALUES (?, ?) ON CONFLICT DO NOTHING`,
			rid, pid,
		); err != nil {
			t.Fatalf("grant %q to %q: %v", p, role, err)
		}
	}
}

// jwtFor creates a user with the given role and returns a fresh JWT for it.
func jwtFor(t *testing.T, db *sql.DB, email, password, role string) string {
	t.Helper()
	ctx := context.Background()
	if _, err := auth.CreateUser(ctx, db, email, password, []string{role}); err != nil {
		t.Fatalf("create user %q: %v", email, err)
	}
	user, err := auth.VerifyCredentials(ctx, db, email, password)
	if err != nil {
		t.Fatalf("verify %q: %v", email, err)
	}
	token, err := auth.IssueJWT(testSecret, user, timeNowUTC())
	if err != nil {
		t.Fatalf("issue jwt: %v", err)
	}
	return token
}

// apiKeyFor mints an API key bound to the given role and returns the plaintext
// secret.
func apiKeyFor(t *testing.T, db *sql.DB, label, role string) string {
	t.Helper()
	rid := roleID(t, db, role)
	secret, err := auth.MintAPIKey(context.Background(), db, label, rid)
	if err != nil {
		t.Fatalf("mint api key: %v", err)
	}
	return secret
}

// authHeader wraps a bearer token into an Authorization header map.
func authHeader(token string) map[string]string {
	return map[string]string{"Authorization": "Bearer " + token}
}

// createArticle POSTs an article as the given token and returns the new id.
func createArticle(t *testing.T, srv *httptest.Server, token, title, body string) string {
	t.Helper()
	status, resp := doJSON(t, srv, http.MethodPost, "/articles",
		map[string]string{"title": title, "body": body}, authHeader(token))
	if status != http.StatusCreated {
		t.Fatalf("create article: status=%d body=%v, want 201", status, resp)
	}
	id, _ := resp["id"].(string)
	if id == "" {
		t.Fatalf("create article: no id in response %v", resp)
	}
	return id
}

