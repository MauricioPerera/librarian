// --- T3/T4: GET /articles and GET /articles/{id} --------------------------

// TestListAndGetArticles covers the read acceptance criterion: any
// authenticated identity (JWT with no perms, AND an API key) → 200;
// unauthenticated → 401.
func TestListAndGetArticles(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.create")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	authorToken := jwtFor(t, db, "au@example.com", "pw", "author") // no perms
	key := apiKeyFor(t, db, "svc", "editor")

	id := createArticle(t, srv, editorToken, "T1", "B1")

	// JWT (no perms) → 200 on list.
	if status, _ := doJSON(t, srv, http.MethodGet, "/articles", nil, authHeader(authorToken)); status != http.StatusOK {
		t.Fatalf("list jwt-noperm status=%d, want 200", status)
	}
	// API key → 200 on list.
	if status, _ := doJSON(t, srv, http.MethodGet, "/articles", nil, authHeader(key)); status != http.StatusOK {
		t.Fatalf("list apikey status=%d, want 200", status)
	}
	// JWT → 200 on get one.
	if status, _ := doJSON(t, srv, http.MethodGet, "/articles/"+id, nil, authHeader(authorToken)); status != http.StatusOK {
		t.Fatalf("get jwt status=%d, want 200", status)
	}
	// Unauthenticated list → 401.
	if status, _ := doJSON(t, srv, http.MethodGet, "/articles", nil, nil); status != http.StatusUnauthorized {
		t.Fatalf("list unauth status=%d, want 401", status)
	}
	// Unauthenticated get one → 401.
	if status, _ := doJSON(t, srv, http.MethodGet, "/articles/"+id, nil, nil); status != http.StatusUnauthorized {
		t.Fatalf("get unauth status=%d, want 401", status)
	}
}

// --- T3/T4: PUT /articles/{id} (content.update) ----------------------------

// TestUpdateArticle covers the update criterion: with content.update → 200 +
// changes persisted; without → 403.
func TestUpdateArticle(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.create", "content.update")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	authorToken := jwtFor(t, db, "au@example.com", "pw", "author") // no update

	id := createArticle(t, srv, editorToken, "Old", "OldBody")

	// With perm → 200 + persisted.
	status, body := doJSON(t, srv, http.MethodPut, "/articles/"+id,
		map[string]string{"title": "New", "body": "NewBody"}, authHeader(editorToken))
	if status != http.StatusOK {
		t.Fatalf("with-perm status=%d body=%v, want 200", status, body)
	}
	var title, artBody string
	if err := db.QueryRow(`SELECT title, body FROM articles WHERE id = ?`, id).Scan(&title, &artBody); err != nil {
		t.Fatalf("select: %v", err)
	}
	if title != "New" || artBody != "NewBody" {
		t.Fatalf("persisted = (%q,%q), want (New,NewBody)", title, artBody)
	}

	// Without perm → 403, and the row is unchanged.
	status, _ = doJSON(t, srv, http.MethodPut, "/articles/"+id,
		map[string]string{"title": "Hack", "body": "Hack"}, authHeader(authorToken))
	if status != http.StatusForbidden {
		t.Fatalf("without-perm status=%d, want 403", status)
	}
	if err := db.QueryRow(`SELECT title FROM articles WHERE id = ?`, id).Scan(&title); err != nil {
		t.Fatalf("select: %v", err)
	}
	if title != "New" {
		t.Fatalf("row changed after 403: title=%q, want New", title)
	}
}

// --- T3/T4: POST /articles/{id}/publish (content.publish) ------------------

// TestPublishArticle covers publish + idempotency: with content.publish → 200
// + published_at set; calling twice does not fail and published_at does not
// change. Without the perm → 403.
func TestPublishArticle(t *testing.T) {
	db, srv, cleanup := openAuthMux(t)
	defer cleanup()
	grant(t, db, "editor", "content.create", "content.publish")
	editorToken := jwtFor(t, db, "ed@example.com", "pw", "editor")
	authorToken := jwtFor(t, db, "au@example.com", "pw", "author") // no publish

	id := createArticle(t, srv, editorToken, "Draft", "Body")

	// Without perm → 403 (published_at still NULL).
	if status, _ := doJSON(t, srv, http.MethodPost, "/articles/"+id+"/publish", nil, authHeader(authorToken)); status != http.StatusForbidden {
		t.Fatalf("without-perm status=%d, want 403", status)
	}

	// First publish → 200, published_at set.
	status, body := doJSON(t, srv, http.MethodPost, "/articles/"+id+"/publish", nil, authHeader(editorToken))
	if status != http.StatusOK {
		t.Fatalf("first publish status=%d body=%v, want 200", status, body)
	}
	first, _ := body["published_at"].(string)
	if first == "" {
		t.Fatalf("first publish published_at empty: %v", body)
	}
	// Confirm NULL→set in the DB.
	var dbPub sql.NullString
	if err := db.QueryRow(`SELECT published_at FROM articles WHERE id = ?`, id).Scan(&dbPub); err != nil {
		t.Fatalf("select: %v", err)
	}
	if !dbPub.Valid || dbPub.String == "" {
		t.Fatalf("published_at not set in DB: %+v", dbPub)
	}

	// Second publish → 200 (no failure), published_at UNCHANGED.
	status, body = doJSON(t, srv, http.MethodPost, "/articles/"+id+"/publish", nil, authHeader(editorToken))
	if status != http.StatusOK {
		t.Fatalf("second publish status=%d, want 200 (idempotent)", status)
	}
	second, _ := body["published_at"].(string)
	if second != first {
		t.Fatalf("idempotency broken: first=%q second=%q", first, second)
	}
}

